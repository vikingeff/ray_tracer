#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		len_s1;
	int		len_s2;

	len_s1 = ft_strlen(s1);
	len_s2 = ft_strlen(s2);
	ft_memcpy(s1 + len_s1, s2, len_s2 + 1);
	return (s1);
}
